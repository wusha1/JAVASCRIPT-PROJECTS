// TYPEOF OPERATOR + document.write()

// Variable to check data type
var myNumber = 42;

// Display the data type of the variable
document.write("Type of myNumber: " + typeof myNumber + "<br><br>");


// STRING + NUMBER 

// Combining a string and a number
document.write("My age is " + 30 + "<br><br>");


// COMPARISON OPERATORS

// Equality operator ==
document.write("10 == 10 : " + (10 == 10) + "<br>");

// Strict equality operator ===
document.write("10 === '10' : " + (10 === "10") + "<br>");

// Greater than operator >
document.write("15 > 10 : " + (15 > 10) + "<br>");

// Less than operator <
document.write("5 < 10 : " + (5 < 10) + "<br><br>");


// LOGICAL OPERATORS

// AND operator &&
document.write("10 > 5 && 5 > 2 : " + (10 > 5 && 5 > 2) + "<br>");

// OR operator ||
document.write("10 < 5 || 5 > 2 : " + (10 < 5 || 5 > 2) + "<br>");

// NOT operator !
document.write("!(10 > 5) : " + !(10 > 5) + "<br>");
